static char temp[XMAX][YMAX];

char main(void)
{
  clock_t       t;
  unsigned long sec;
  unsigned      sec10;
  unsigned long fps;
  unsigned      fps10;
  unsigned char background;
  unsigned char text;
        
	char x;
	char y;
	char xR;
	char xL;
	char yO;
	char yU;
	char lebende;
	unsigned char round = 0;

	t = clock ();
	clrscr();
	background = bgcolor(COLOR_BLACK);
	text = textcolor(COLOR_WHITE);
	printSpielfeld(spielfeld);
//	signal (int sig, __sigfunc func);


	while(round < ROUNDS && !kbhit()){
		for(y = 0; y< YMAX; y++){
			for(x = 0; x< XMAX; x++){
				yO = y + 1;
				yU = y - 1;
				xL = x - 1;
				xR = x + 1;

				if (x == 0) {
					xL = XMAX - 1;
				}
				if (x == XMAX - 1) {
					xR = 0;
				}
				if (y == 0) {
					yO = YMAX - 1;
				}
				if (y == YMAX - 1) {
					yU = 0;
				}

				gotoxy(0,0);
				lebende = 0;
				lebende += spielfeld[xL][yO];
				lebende += spielfeld[x][yO];
				lebende += spielfeld[xR][yO];
				lebende += spielfeld[xL][y];
				lebende += spielfeld[xR][y];
				lebende += spielfeld[xL][yU];
				lebende += spielfeld[x][yU];
				lebende += spielfeld[xR][yU];


				switch (lebende)
				{
				case 2: temp[x][y] = spielfeld[x][y];
					break;

				case 3: temp[x][y] = 1;
					break;

				default: temp[x][y] = 0;
					break;
				}
				//findnachbarn
				//lebende = zaehlLebende(nachbarn);
				gotoxy(x,y);
			}// for x
		}// for y

		memcpy(spielfeld,temp,XMAX*YMAX);
	
		round++;
		printSpielfeld(spielfeld);	
	}
		t = clock() - t;
	
  /* Reset screen colors */
    bgcolor (background);
    textcolor (text);
    clrscr ();

    /* Calculate stats */
    sec   = (t * 10) / CLK_TCK;
    sec10 = sec % 10;
    sec  /= 10;
    fps   = (round * (CLK_TCK * 10)) / t;
    fps10 = fps % 10;
    fps  /= 10;

    /* Output stats */
    gotoxy (0, 0); cprintf ("time  : %lu.%us", sec, sec10);
    gotoxy (0, 1); cprintf ("frames: %lu", round);
    gotoxy (0, 2); cprintf ("fps   : %lu.%u", fps, fps10);

    /* Wait for a key, then end */
    cputsxy (0, 4, "Press any key when done...");
    (void) cgetc ();

    /* Done */
    return EXIT_SUCCESS;
}


